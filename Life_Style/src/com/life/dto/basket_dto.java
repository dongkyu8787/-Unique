package com.life.dto;

public class basket_dto {
	private String basket_id;
	private int basket_no_seq;
	private int basket_productno;
	private int basket_price;
	private int basket_count;
	public String getBasket_id() {
		return basket_id;
	}
	public void setBasket_id(String basket_id) {
		this.basket_id = basket_id;
	}
	public int getBasket_no_seq() {
		return basket_no_seq;
	}
	public void setBasket_no_seq(int basket_no_seq) {
		this.basket_no_seq = basket_no_seq;
	}
	public int getBasket_productno() {
		return basket_productno;
	}
	public void setBasket_productno(int basket_productno) {
		this.basket_productno = basket_productno;
	}
	public int getBasket_price() {
		return basket_price;
	}
	public void setBasket_price(int basket_price) {
		this.basket_price = basket_price;
	}
	public int getBasket_count() {
		return basket_count;
	}
	public void setBasket_count(int basket_count) {
		this.basket_count = basket_count;
	}
	
	
}
