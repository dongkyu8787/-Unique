package com.life.dto;

public class map_dto {
	private int map_no_seq;
	private String map_place;
	private double map_latitude;
	private double map_longitude;
	public int getMap_no_seq() {
		return map_no_seq;
	}
	public void setMap_no_seq(int map_no_seq) {
		this.map_no_seq = map_no_seq;
	}
	public String getMap_place() {
		return map_place;
	}
	public void setMap_place(String map_place) {
		this.map_place = map_place;
	}
	public double getMap_latitude() {
		return map_latitude;
	}
	public void setMap_latitude(double map_latitude) {
		this.map_latitude = map_latitude;
	}
	public double getMap_longitude() {
		return map_longitude;
	}
	public void setMap_longitude(double map_longitude) {
		this.map_longitude = map_longitude;
	}
}
